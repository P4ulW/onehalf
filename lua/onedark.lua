---@param config onedarkConfig?
onedark.setup = function(config)
    onedark.config = vim.deepcopy(default_config)
    onedark.config = vim.tbl_deep_extend("force", onedark.config, config or {})
end

--- main load function
onedark.load = function()
    if vim.version().minor < 8 then
        vim.notify_once("onedark.nvim: you must use neovim 0.8 or higher")
        return
    end

    -- reset colors
    if vim.g.colors_name then
        vim.cmd.hi("clear")
    end
    vim.g.colors_name = "onedark"
    vim.o.termguicolors = true

    local groups = get_groups()

    -- add highlights
    for group, settings in pairs(groups) do
        vim.api.nvim_set_hl(0, group, settings)
    end
end

return onedark
